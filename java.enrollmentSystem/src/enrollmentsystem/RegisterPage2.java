    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        // registration
        String dob = jTextField1.getText();
        String address = jTextField2.getText();
        String course = String.valueOf(jComboBox1.getSelectedItem());
        String YrAndSec = jTextField3.getText();
        String Cnumber = jTextField4.getText();
        
        try
        {
            conn = DriverManager.getConnection(url, "root", "$4Y my n4m3");
            pst = conn.prepareStatement("UPDATE `enrollmentsys`.`logindatastudent` SET `DOB` = ?, `Address` = ?, `Course` = ?, `YrSec` = ?, `ContactNum` = ? WHERE `StudentID` = ?");
            
            pst.setString(1, dob);
            pst.setString(2, address);
            pst.setString(3, course);
            pst.setString(4, YrAndSec);
            pst.setString(5, Cnumber);
            
            if (Boolean.valueOf(dob.equals("")) || Boolean.valueOf(address.equals("")) || Boolean.valueOf(course.equals("")) || Boolean.valueOf(YrAndSec.equals("")) || Boolean.valueOf(Cnumber.equals("")))
            {
                JOptionPane.showMessageDialog(null, "please fill out all fields.", "Registration", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                
                pst.setString(6, JOptionPane.showInputDialog(this, "Confirm StudentID: ", "Student ID", JOptionPane.OK_CANCEL_OPTION));
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Registration complete.", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

